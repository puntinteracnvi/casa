function generateWords() {
    const container = document.querySelector(".image-wrapper");

    words.forEach(word => {
        const wordDiv = document.createElement("div");
        wordDiv.className = "word";
        wordDiv.style.top = word.top;
        wordDiv.style.left = word.left;
        wordDiv.onclick = () => playAudio(word.id);

        wordDiv.innerHTML = `
            <span class="catalan-word" style="color: ${word.style?.color || "#000"}; 
                                               font-size: ${word.style?.fontSize || "0.6em"}; 
                                               font-weight: ${word.style?.fontWeight || "bold"};
                                               display: block; line-height: 1;">
                ${word.catalan}
            </span>
            <div class="translation" id="${word.id}-translation" style="line-height: 1;">
                ${word.translations.English}
            </div>
        `;

        // Create and append audio element dynamically
        const audioElement = document.createElement("audio");
        audioElement.id = `audio-${word.id}`;
        audioElement.src = word.audio;
        container.appendChild(audioElement);

        container.appendChild(wordDiv);
    });
}



function changeAllTranslations(language) {
    words.forEach(word => {
        document.getElementById(`${word.id}-translation`).textContent = word.translations[language];
    });
}

function playAudio(wordId) {
    let audioElement = document.getElementById(`audio-${wordId}`);
    if (audioElement) {
        audioElement.play();
    }
}

// Generate words on page load
document.addEventListener("DOMContentLoaded", generateWords);






